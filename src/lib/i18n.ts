// 语言切换工具函数
export const i18n = {
  // 获取当前语言
  getCurrentLanguage(): Language {
    if (typeof window === 'undefined') return defaultLanguage;
    
    const saved = localStorage.getItem('preferred-language');
    if (saved && supportedLanguages.includes(saved as Language)) {
      return saved as Language;
    }
    
    const browserLang = navigator.language;
    const matched = supportedLanguages.find(lang => 
      browserLang.startsWith(lang.split('-')[0])
    );
    
    return matched || defaultLanguage;
  },
  
  // 设置语言
  setLanguage(lang: Language): void {
    if (typeof window === 'undefined') return;
    
    localStorage.setItem('preferred-language', lang);
    document.documentElement.lang = lang;
    
    // 触发storage事件，让其他标签页也能感知语言变化
    window.dispatchEvent(new StorageEvent('storage', {
      key: 'preferred-language',
      newValue: lang,
      oldValue: localStorage.getItem('preferred-language')
    }));
    
    // 触发自定义事件，确保当前页面的所有组件都能响应
    window.dispatchEvent(new Event('languageChanged'));
  },
  
  // 翻译文本
  t(key: string, lang?: Language): string {
    const currentLang = lang || this.getCurrentLanguage();
    return translations[currentLang]?.[key] || translations[defaultLanguage]?.[key] || key;
  }
};

// 初始化语言设置
export const initializeI18n = () => {
  if (typeof window !== 'undefined') {
    const lang = i18n.getCurrentLanguage();
    i18n.setLanguage(lang);
  }
};